#include "ft_printf.h"

int	ft_putnbr(long n)
{
	int	res;

	res = 0;
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		res += ft_putchar('-');
		n *= -1;
	}
	if (n / 10)
		res += ft_putnbr(n / 10);
	res += ft_putchar(n % 10 + '0');
	return (res);
}
